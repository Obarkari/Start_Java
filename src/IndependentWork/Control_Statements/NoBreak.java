package IndependentWork.Control_Statements;

public class NoBreak {
    public static void main(String[] args) {
        int i;

        for (i=0; i<=5; i++) {
            switch (i) {
                case 0:
                    System.out.println("i меньше единицы");
                case 1:
                    System.out.println("i меньше друх");
                case 2:
                    System.out.println("i меньше трёх");
                case 3:
                    System.out.println("i меньше четырёх");
                case 4:
                    System.out.println("i меньше пяти");
            }
                    System.out.println();

            }
    }
}

